    private void BtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAddActionPerformed
        // TODO add your handling code here:
        try{
            nodo = new Nodo();
            aux = new Nodo();
            try{
                nodo.setId(Integer.parseInt(txtDisplayId.getText()));
                aux = oblista.Buscar(nodo);
                if(aux != null){
                    JOptionPane.showMessageDialog(this,"id no valido");
                }
                nodo.setNombre(txtDisplayNombre.getText());
                txtDisplayNombre.setText("");
                txtDisplayId.setText("");
            }catch(Exception Ex){
                JOptionPane.showMessageDialog(this,"numero/nombre no validos");
            }
            
            oblista.AddNodo(nodo);
            lista = oblista.Actualizar();
        }catch(Exception Ex){
            JOptionPane.showMessageDialog(this,"no se logro agregar");
        }
    }//GEN-LAST:event_BtnAddActionPerformed

    private void BtnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEditActionPerformed
        // TODO add your handling code here:
        try{
            nodo = new Nodo();
            nodo.setId(Integer.parseInt(txtDisplayBusqueda.getText()));
            nodo = oblista.Buscar(nodo);
            aux = nodo;
            if(aux != null){
                aux.setId(Integer.parseInt(txtDisplayBusqueda.getText()));
                aux.setNombre(txtDisplayNombre.getText());
                oblista.AddNodo(aux); 
                txtDisplayNombre.setText("");
                txtDisplayId.setText("");
                oblista.deleteNodo(nodo);
                lista = oblista.Actualizar();
            }
            else{
                JOptionPane.showMessageDialog(this,"no existe");
            }
            
        }catch(Exception Ex){
            JOptionPane.showMessageDialog(this,"no se edito");
        } 
        
    }//GEN-LAST:event_BtnEditActionPerformed

