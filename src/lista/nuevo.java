    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            nodo = new Nodo();
            nodo.setId(Integer.parseInt(txtDisplayBusqueda.getText()));
            nodo = oblista.Buscar(nodo);
            if(nodo != null){
                txtDisplayId.setText(String.valueOf(nodo.getId()));
                txtDisplayNombre.setText(nodo.getNombre());
            }
            else{
                JOptionPane.showMessageDialog(this,"no existe");
            } 
        }catch(Exception Ex){
            JOptionPane.showMessageDialog(this,"una letra");
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void BtnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRemoveActionPerformed
        // TODO add your handling code here:
        nodo = new Nodo();
        nodo.setId(Integer.parseInt(txtDisplayBusqueda.getText()));
        nodo = oblista.Buscar(nodo);
        if(nodo!=null){
            oblista.deleteNodo(nodo);
            lista = oblista.Actualizar();
            txtDisplayNombre.setText("");
            txtDisplayId.setText("");
            System.out.println("supuestamente se elimino");
        }
        else{
            JOptionPane.showMessageDialog(this,"no se encontro");
        }
    }//GEN-LAST:event_BtnRemoveActionPerformed

